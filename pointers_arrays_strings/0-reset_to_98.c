#include <stdio.h>
/**
 * reset_to_98 - Updates the value of the integer pointed to by n to 98.
 * @n: Pointer to an int to update
 *
 * Description: This function takes a pointer to an integer as a parameter
 * and sets the value it points to to 98.
 */
void reset_to_98(int *n)
{
	*n = 98;
}
